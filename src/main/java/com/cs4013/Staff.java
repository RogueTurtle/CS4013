package com.cs4013;

public class Staff extends Person{
    Staff(String name, int phoneNum){
        super(name, phoneNum);
    }

    
    /** 
     * @return String
     */
    @Override
    public String getName() {
        
        return super.getName();
    }

    
    /** 
     * @return int
     */
    @Override
    public int getPhoneNum() {
        return super.getPhoneNum();
    }
    
}
